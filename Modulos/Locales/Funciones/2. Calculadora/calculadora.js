const sumar = function(a,b){
    return a+b
}

const restar = function(a,b){
    return a-b
}

const multiplicar = function(a,b){
    return a*b
}

const dividir = function(a,b){
    return a/b
}

module.exports = {
    sumar: sumar,
    restar: restar,
    multiplicar: multiplicar,
    dividir: dividir,
}